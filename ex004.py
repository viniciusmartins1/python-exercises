a = input('Digite algo: ')
print(type(a))
print('É alphanumérico: ', a.isalnum())
print('É alphabético: ', a.isalpha())
print('É digitável: ', a.isdigit())
print('É decimal: ', a.isdecimal())
print('É maiúsculo: ', a.islower())
print('Tem espaço: ', a.isspace())
print('É minúsculo: ', a.isupper())




